    nodes = []
    for line in sample_edge_data.split('\n'):
        if line.startswith('from_id') or not line.strip():
            continue
        a,b = line.split()
        edges.append((a,b))
        nodes += [a,b]
    edges = list(set(edges))
    nodes = list(set(nodes))
    ed = {e:True for e in edges}
    if drivers:
        dd = {e:True for e in drivers}
    else:
        dd={}
    if responses:
        rd = {e:True for e in responses}
    else:
        rd={}
    return (dd, rd, ed, edges, nodes)



